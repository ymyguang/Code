def yh():
    L = [1]
    yield L
    while True:
        L1 = [0] + L
        L2 = L + [0]
        L.append(0)
        for m in range(len(L)):
            L[m] = (L1[m] + L2[m])
        yield L
        
results = []
n = 0
for t in yh():
    results.append(t)
    n = n + 1
